# Processing
dat <- unique(dat)
dat <- dat[order(dat$id),]
dat <- dat[!is.na(dat$coef),]
row.names(dat) <- NULL
levels(dat$elecsys2) <- c('Majoritarian', 'Non-Majoritarian')
dat$scoef <- factor(as.character(dat$scoef), labels = c('N', 'P'))
dat$scoefsig <- factor(as.character(dat$scoefsig), labels = c('N', 'P'))
levels(dat$scoef) <- c('Negative', 'Positive')
levels(dat$scoefsig) <- c('Negative', 'Positive')
dat$method <- factor(as.character(dat$method), 
                     levels = c("OLS", "PANEL", "IV", "RDD"))
dat$method2 <- factor(as.character(dat$method2), 
                     levels = c("OLS/PANEL", "IV", "RDD"))
dat$location2 <- as.character(dat$location)
dat$location2[dat$location2!='World'] = 'Country'
dat$location2 <- factor(as.character(dat$location2), 
                        levels = c("Country", "World"))
dat$agglevel <- factor(as.character(dat$agglevel), 
                       levels = c("Local", "States", "Countries"))
dat$agglevel2 <- dat$agglevel
levels(dat$agglevel2) = c("Country", "Country", "Cross-Country")
dat$scoefnd <- as.character(dat$scoef)
dat$scoefnd[dat$scoefsig=='Positive'&dat$scoef=='Positive'&!is.na(dat$scoefsig)] = 'Positive Significant'
dat$scoefnd[dat$scoefnd=='Positive'] = 'Positive Insignificant'
dat$scoefnd[dat$scoefsig=='Negative'&dat$scoef=='Negative'&!is.na(dat$scoefsig)] = 'Negative Significant'
dat$scoefnd[dat$scoefnd=='Negative'] = 'Negative Insignificant'

# Create indicator
dat <- arrange(dat, id) %>%
  mutate(id_paper = rleid(id)) %>%
  mutate(id_level1 = ifelse(id %in% c('3', '42', '132', '165',
                                      '439', '441', '467', '505'), '3-505', 
                            ifelse(id %in% c('408', '208', 'A258'), '408-A258', 
                                   ifelse(id %in% c('849', '578'), '849-578', id)))) %>%
  group_by(id_paper) %>%
  mutate(id_model = row_number()) %>%
  ungroup() %>%
  group_by(id_level1) %>%
  mutate(id_level2 = row_number()) %>%
  ungroup() %>%
  relocate(id_paper, .after = id) %>%
  relocate(id_model, .after = id_paper) %>%
  relocate(id_level1, .after = id_model) %>%
  relocate(id_level2, .after = id_level1) %>%
  mutate(VAR = SE^2)

